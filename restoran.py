        cur.execute("SELECT * FROM restoranNaplata")
        rows=cur.fetchall()
        if(len(rows)!=0):
            self.ispis.delete(*self.ispis.get_children())
            for row in rows:
                self.ispis.insert('',END,values=row)
                con.commit()


