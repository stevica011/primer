    def novaporudžbina(self):
        self.skidanjestavke()
        self.tura()
        if self.var_idgosti.get()==0:
            messagebox.showerror("Greška", "Unesite id gosta kome je porudžbina namenjena!", parent=self.root)
        elif self.var_stanje.get()==0 or self.var_statusR.get()!="otvoren" :
            messagebox.showerror("Greška", "Račun ne postoji ili je naplaćen!", parent=self.root)
        elif self.var_statusD.get()!="otvoren":
                messagebox.showerror("Greška", "Račun mora biti unet u tekući dan!", parent=self.root)
        else:
            self.var_stanje.set(self.var_stanje.get()+self.var_tura.get())
            try:
                con=Konekcija.getInstance()
                cursor=con.cursor()
                cursor.execute("UPDATE restoranNaplata SET trenutnostanje=%s  WHERE brojračuna=%s",(self.var_stanje.get(),self.var_brojračuna.get()))
                con.commit()
                messagebox.showinfo("OK","Uspešna porudžbina",parent=self.root)
                self.poništi()
                self.prikažiSveRačune()
            except Exception as es:
                messagebox.showwarning("Upozorenje",f"Nešto je pošlo loše:{es}",parent=self.root)
        

    def obriširačun(self):
        if self.var_total.get()!=0:
            messagebox.showerror("Greška", "Račun je već naplaćen", parent=self.root)
        else:
            obriši=messagebox.askyesno("Restoran","Da li zaista želite da obrišete ovaj račun?")
            if obriši>0:
                con=Konekcija.getInstance()
                cur=con.cursor()
                cur.execute("DELETE FROM restoranNaplata  WHERE brojračuna=%s",(self.var_brojračuna.get(),))
                con.commit()
                messagebox.showinfo("Uspešno obrisan račun!","Brisanje uspelo!")
                self.poništi()
                self.prikažiSveRačune()
        

